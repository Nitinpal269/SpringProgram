package com.spring.practice.ci;

public class Certificate {
	
	public String java;
	public String c;
	public Certificate(String java, String c) {
		super();
		this.java = java;
		this.c = c;
	}
	@Override
	public String toString() {
		return "Certificate [java=" + java + ", c=" + c + "]";
	}
	
	
	
	
	
	
	

}
